export const stockSlice = createSlice({
  name: 'stocks',
  initialState,
  reducers: {
    setCurrentStock: (state, action: PayloadAction<{ symbol: string; date: string }>) => {
      // @ts-ignore
      state.currentStock = action.payload;
    },
    setActiveStock: (state, action: PayloadAction<UserStock>) => {
      let counter = true;

      state.activeStocks.forEach((ev) => {
        if (ev.symbol === action.payload.symbol) {
          counter = false;
        }
      });
      if (counter) {
        // @ts-ignore
        state.activeStocks = [...state.activeStocks, action.payload];
      }
    },
    filterStockNames: (state, action: PayloadAction<string>) => {
      state.stocksNames = state.stocksNames.filter((el) => el.shortName.includes(action.payload));
    },
    filterUserStocks: (state, action: PayloadAction<string>) => {
      state.userStocks = state.userStocks.filter((el) => el.shortName.includes(action.payload));
    },
    deleteStock: (state, action: PayloadAction<UserStock>) => {
      state.activeStocks = state.activeStocks.filter((ev) => ev.symbol !== action.payload.symbol);
    },
  },
  extraReducers: {
    [setStockThunk.fulfilled.type]: (state, action: PayloadAction<Array<UserStock>>) => {
      console.log('==========>action', action);
      // @ts-ignore
      state.stocksNames = action.payload;
    },
    [setUserStockThunk.fulfilled.type]: (state, action: PayloadAction<Array<UserStock>>) => {
      let newData: any[] = [];
      events.forEach((el) => {
        if (el.dividends?.length) {
          newData = [...newData, ...el.dividends];
        }
      });
      state.dividends = newData;
      state.userStocks = action.payload;
    },
    [deleteUserStockThunk.fulfilled.type]: (state, action: PayloadAction<Array<UserStock>>) => {
      state.userStocks = action.payload;
    },
    [addUserStockThunk.fulfilled.type]: (state, action: PayloadAction<Array<UserStock>>) => {
      state.userStocks = action.payload;
    },
  },
});

export const { setActiveStock, deleteStock, filterStockNames, filterUserStocks, setCurrentStock } = stockSlice.actions;

export default stockSlice.reducer;
